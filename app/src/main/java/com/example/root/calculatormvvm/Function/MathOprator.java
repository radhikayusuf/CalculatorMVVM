public class MathOprator {
    public double oprasiTambah(double num1,double num2){
        return num1+num2;
    }
    public double oprasiKurang(double num1,double num2){
        return num1-num2;
    }
    public double oprasiKali(double num1,double num2){
        return num1*num2;
    }
    public double oprasiBagi(double num1,double num2){
        double hasil = num1 / num2;
        return hasil;
    }

}
